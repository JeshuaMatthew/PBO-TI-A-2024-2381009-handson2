package binaryoperator;

// biasakan penulisan package hanya menggunakan huruf kecil

public class Aritmatika {
    public static void main(String[] args) {
        int Operand1 = 20;
        int Operand2 = 4;

        // binary operator adalah operator yang memerlukan 2 operand

        System.out.println(Operand1 + Operand2);
        System.out.println(Operand1 - Operand2);
        System.out.println(Operand1 * Operand2);
        System.out.println(Operand1 / Operand2);
        System.out.println(Operand1 % Operand2);
    }
}
